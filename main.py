def відобразити_лінію(довжина, напрямок, символ):
   if напрямок == "горизонтальний":
      print(символ * довжина)
   elif напрямок == "вертикальний":
      for _ in range(довжина):
         print(символ)
   else:
      print("Неправильний напрямок. Виберіть 'горизонтальний' або 'вертикальний'.")

відобразити_лінію(10, "вертикальний", "*")

